class LinkedDeQueueImpl<E> implements IDeque<E> {

    private final TwoSideLinkedList<E> data;

    public LinkedDeQueueImpl() {
        this.data = new TwoSideLinkedListImpl<>();
    }



    public E remove() {
        return data.removeFirst();
    }

    @Override
    public E peekFront() {
        return data.getFirst();
    }

    @Override
    public int size() {
        return data.size();
    }

    @Override
    public boolean isEmpty() {
        return data.isEmpty();
    }

    @Override
    public boolean isFull() {
        return false;
    }

    @Override
    public void display() {
        data.display();
    }

    @Override
    public String toString() {
        return "LinkedQueueImpl{" + "data=" + data + '}';
    }

    @Override
    public boolean insertLeft(E value) {
        data.insertFirst(value);
        return true;
    }

    @Override
    public boolean insertRight(E value) {
        data.insertLast(value);
        return true;
    }

    @Override
    public E removeLeft() {
        return data.removeFirst();
    }

    @Override
    public boolean removeRight() {
        return data.removeRight();

}
}
